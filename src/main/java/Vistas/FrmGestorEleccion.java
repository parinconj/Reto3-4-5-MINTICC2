    private void botonEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonEditarActionPerformed

        this.botonActualizar.setVisible(true);
        this.campoFechaFin.setVisible(true);
        this.labelFechaFin.setVisible(true);

        int columna = 0;
        int fila = this.tablaElecciones.getSelectedRow();

        String idEleccion = this.tablaElecciones.getModel().getValueAt(fila, columna).toString();

        ClsEleccion eleccion = this.BuscarEleccion(idEleccion);

        //           JOptionPane.showMessageDialog(null, eleccion.getEstado());
        if (eleccion != null) {

            this.campoDescripcion.setText(eleccion.getDescripcion());
            this.comboTipo.setSelectedItem(eleccion.getTipo());

        }

        // TODO add your handling code here:
    }//GEN-LAST:event_botonEditarActionPerformed

    public void ObtenerElecciones() {

        this.elecciones = this.controladorEleccion.obtenerElecciones();
        ActualizarTabla(elecciones);
    }

    public ClsEleccion BuscarEleccion(String idEleccion) {

        for (ClsEleccion eleccion : this.elecciones) {

            if (eleccion.getIdEleccion().equals(idEleccion)) {

                return eleccion;
            }

        }

        return null;

    }

    private void ActualizarTabla(LinkedList<ClsEleccion> lista) {

        DefaultTableModel modelo = (DefaultTableModel) tablaElecciones.getModel();
        modelo.setRowCount(0);

        for (ClsEleccion c : lista) {

            Object[] fila = {c.getIdEleccion(), c.getDescripcion(), c.getTipo(), c.getFechaInicio(), c.getFechaFin(),
                c.getEstado()};

            modelo.addRow(fila);

        }

    }

